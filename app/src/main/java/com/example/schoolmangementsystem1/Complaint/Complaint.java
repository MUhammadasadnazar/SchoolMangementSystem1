package com.example.schoolmangementsystem1.Complaint;

public class Complaint {

	String ComplaintId;
	String StdID;
	String StdName;
	String StdRollno;
	String ComplaintTitle;
	String ComplaintDetail;
	String ComplaintTimeStamp;

	public Complaint() {
	}

	public String getComplaintId() {
		return ComplaintId;
	}

	public void setComplaintId(String complaintId) {
		ComplaintId = complaintId;
	}

	public String getStdID() {
		return StdID;
	}

	public void setStdID(String stdID) {
		StdID = stdID;
	}

	public String getStdName() {
		return StdName;
	}

	public void setStdName(String stdName) {
		StdName = stdName;
	}

	public String getStdRollno() {
		return StdRollno;
	}

	public void setStdRollno(String stdRollno) {
		StdRollno = stdRollno;
	}

	public String getComplaintTitle() {
		return ComplaintTitle;
	}

	public void setComplaintTitle(String complaintTitle) {
		ComplaintTitle = complaintTitle;
	}

	public String getComplaintDetail() {
		return ComplaintDetail;
	}

	public void setComplaintDetail(String complaintDetail) {
		ComplaintDetail = complaintDetail;
	}

	public String getComplaintTimeStamp() {
		return ComplaintTimeStamp;
	}

	public void setComplaintTimeStamp(String complaintTimeStamp) {
		ComplaintTimeStamp = complaintTimeStamp;
	}
}
