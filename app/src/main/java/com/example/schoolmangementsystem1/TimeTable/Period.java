package com.example.schoolmangementsystem1.TimeTable;

public class Period {
	String lectureTimeStamp;
	String lectureTimeStart ;
	String lectureTimeEnd;
	String lectureType;
	String lectureSubject;
	String lectureSubjectID;
	String lectureInstructureName;
	String lectureInstructureId;
	int lectureNo;

	public Period() {
	}

	public String getLectureTimeStamp() {
		return lectureTimeStamp;
	}

	public void setLectureTimeStamp(String lectureTimeStamp) {
		this.lectureTimeStamp = lectureTimeStamp;
	}

	public String getLectureTimeStart() {
		return lectureTimeStart;
	}

	public void setLectureTimeStart(String lectureTimeStart) {
		this.lectureTimeStart = lectureTimeStart;
	}

	public String getLectureTimeEnd() {
		return lectureTimeEnd;
	}

	public void setLectureTimeEnd(String lectureTimeEnd) {
		this.lectureTimeEnd = lectureTimeEnd;
	}

	public String getLectureType() {
		return lectureType;
	}

	public void setLectureType(String lectureType) {
		this.lectureType = lectureType;
	}

	public String getLectureSubject() {
		return lectureSubject;
	}

	public void setLectureSubject(String lectureSubject) {
		this.lectureSubject = lectureSubject;
	}

	public String getLectureSubjectID() {
		return lectureSubjectID;
	}

	public void setLectureSubjectID(String lectureSubjectID) {
		this.lectureSubjectID = lectureSubjectID;
	}

	public String getLectureInstructureName() {
		return lectureInstructureName;
	}

	public void setLectureInstructureName(String lectureInstructureName) {
		this.lectureInstructureName = lectureInstructureName;
	}

	public String getLectureInstructureId() {
		return lectureInstructureId;
	}

	public void setLectureInstructureId(String lectureInstructureId) {
		this.lectureInstructureId = lectureInstructureId;
	}

	public int getLectureNo() {
		return lectureNo;
	}

	public void setLectureNo(int lectureNo) {
		this.lectureNo = lectureNo;
	}
}
