package com.example.schoolmangementsystem1.Attendance;

public class Attendance {
	private String TimeStamp;
	private String StdrollNo;
	private String StdUid;
	private String StdName;
	private String Status;
	private String AttId;
	private String Attmonth;
	private String AttDate;
	private String AttYear;

	public Attendance() {
	}

	public String getTimeStamp() {
		return TimeStamp;
	}

	public void setTimeStamp(String timeStamp) {
		TimeStamp = timeStamp;
	}

	public String getStdrollNo() {
		return StdrollNo;
	}

	public void setStdrollNo(String stdrollNo) {
		StdrollNo = stdrollNo;
	}

	public String getStdUid() {
		return StdUid;
	}

	public void setStdUid(String stdUid) {
		StdUid = stdUid;
	}

	public String getStdName() {
		return StdName;
	}

	public void setStdName(String stdName) {
		StdName = stdName;
	}

	public String getStatus() {
		return Status;
	}

	public void setStatus(String status) {
		Status = status;
	}

	public String getAttId() {
		return AttId;
	}

	public void setAttId(String attId) {
		AttId = attId;
	}

	public String getAttmonth() {
		return Attmonth;
	}

	public void setAttmonth(String attmonth) {
		Attmonth = attmonth;
	}

	public String getAttDate() {
		return AttDate;
	}

	public void setAttDate(String attDate) {
		AttDate = attDate;
	}

	public String getAttYear() {
		return AttYear;
	}

	public void setAttYear(String attYear) {
		AttYear = attYear;
	}
}
